package ex_09_Switch;

public class Lab_05_Interview_Char {
    public static void main(String[] args) {

        char code = 'C';

        switch(code)
        {
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
            default:
                System.out.println("Hello, no amount matching");
        }


    }
}
